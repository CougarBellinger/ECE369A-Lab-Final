            bne $t2, $zero, sign_change             # jump to sign change

        sum:    
            add $s4, $s4, $t0               # add to absolute sum
            lw $t4, 0($sp)
            lw $t2, 4($sp)
            lw $t0, 8($sp)
            addi $sp, $sp, 12
            jr $ra

        sign_change:
            addi $t2, $zero, -1 
            mul $t0, $t0, $t2               # change negative to positive
            j sum