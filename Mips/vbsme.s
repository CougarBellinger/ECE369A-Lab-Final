                j iter_tb 
        
        loop_l2:
                slt $t8, $s3, $t2       # l < window height
                beq $t8, $zero, compare_tb # go to loop_tb when l >= window height
                li $s2, 0               # k = 0
                j loop_k2

        iter_l2:
                addi $s3, $s3, 1        # l++
                j loop_l2
        
        loop_k2:
                slt $t8, $s2, $t3       # k < window width
                beq $t8, $zero, iter_l2 # go to iter_l2 when k >= window width
                jal absolute
                addi $s2, $s2, 1        # k++
                j loop_k2

        exit_tb:
                move $s1, $t7           # j = bottom boundary
        

        ###########################
        # RIGHT TO LEFT
        ###########################

        move $s0, $t6             # i = right boundary    
        loop_rl:
                
                # beq $s0, $t5, exit_rl   # if i == lb, exit
                slt $t8, $t5, $s0      # lB < i (i >= lb) 
                beq $t8, $zero, exit_rl # branch if i < lb

                li $s4, 0               # SAD = 0
                li $s3, 0               # l = 0
                j loop_l3

        compare_rl:
                slt $t8 , $s4, $s5      # sumABS < minABS
                beq $s4, $s5, iter_rl   # don't update if same value  
                bne $t8, $zero, newABS_rl
        iter_rl:
                addi $s0, $s0, -1       # i--
                j loop_rl               

        newABS_rl:
                move $v0, $s1           # coordinates[0] = j;
                move $v1, $s0           # coordinates[1] = i;
                move $s5, $s4           # minABS = sumABS
                j iter_rl

        loop_l3:
                slt $t8, $s3, $t2        # l < window height,  need to finish
                beq $t8, $zero, compare_rl # go to loop_i when l >= window height
                li $s2, 0               # k = 0
                j loop_k3

        iter_l3:
                addi $s3, $s3, 1        # l++
                j loop_l3
        
        loop_k3:
                slt $t8, $s2, $t3       # k < window width
                beq $t8, $zero, iter_l3 # go to iter_l1 when k >= window width 
                jal absolute
                addi $s2, $s2, 1        # k++
                j loop_k3

        exit_rl:
                move $s0, $t5           # i = leftBoundary
                addi $t4, $t4, 1        # topBoundary += 1

        
        ###########################
        # BOTTOM TO TOP
        ###########################

        move $s1, $t7                   # j = bottom boundary    
        loop_bt:
                # beq $s1, $t7, exit_bt   # if j == bb, exit
                slt $t8, $t5, $s1      # lB < j (j >= bb) 
                beq $t8, $zero, exit_bt # branch if j < bb

                li $s4, 0               # SAD = 0
                li $s3, 0               # l = 0
                j loop_l4               # jump to l loop

        compare_bt:
                slt $t8 , $s4, $s5      # sumABS < minABS
                beq $s4, $s5, iter_bt   # don't update if same value  
                bne $t8, $zero, newABS_bt

        iter_bt:
                addi $s1, $s1, -1       # --j
                j loop_bt

        newABS_bt:
                move $v0, $s1           # coordinates[0] = j;
                move $v1, $s0           # coordinates[1] = i;
                move $s5, $s4           # minABS = sumABS
                j iter_bt

        loop_l4:
                slt $t8, $s3, $t2        # l < window height,  need to finish
                beq $t8, $zero, compare_bt # go to loop_i when l >= window height
                li $s2, 0               # k = 0
                j loop_k4

        iter_l4:
                addi $s3, $s3, 1        # l++
                j loop_l4
        
        loop_k4:
                slt $t8, $s2, $t3       # k < window width
                beq $t8, $zero, iter_l4 # go to iter_l1 when k >= window width 
                jal absolute
                addi $s2, $s2, 1        # k++
                j loop_k4

        exit_bt:
                move $s1, $t4           # j = top boundary
                addi $t7, $t7, -1       # bottom boundary =-1
                addi $t6, $t6, -1       # right boundary =-1
                li $s6, 1               # secondrun = 1

        j while

        exit:
                lw $ra, 0($sp)          #reset return address with ra when vbsme is called
                addi $sp, $sp, 4        #reset stack pointer to bottom
                jr $ra                  #jump to return

        absolute:                       # need to maintain framewidth = t1, window width = $t3, i = s0, j = s1, k = s2, l = s3
        addi $sp, $sp, -12
        sw $t0, 8($sp)
        sw $t2, 4($sp)
        sw $t4, 0($sp)

        add $t0, $a1, $zero             # t0 = &frame[]

        #sll $t2, $s0, 2                # make i word readable
        #add $t0, $t0, $t2              # t0 = &frame[i]

        add $t2, $s1, $s3               # t2 = (l + j)
        mul $t2, $t1, $t2               # t2 = (l + j) * framewidth
        add $t2, $t2, $s2               # t2 = (l + j) * framewidth) + k
        add $t2, $t2, $s0               # t2 = i + ((l + j) * framewidth) + k

        sll $t2, $t2, 2                 # make t2 word readable
        add $t0, $t0, $t2               # t0 = &frame[i + ((l + j) * framewidth) + k]

        lw $t4, 0($t0)                  # t4 = frame[i + ((l + j) * framewidth) + k]

        add $t0, $a2, $zero             # t0 = &window[]
        mul $t2, $s3, $t3               # t2 = (l * windowWidth)
        add $t2, $t2, $s2               # t2 = (l * windowWidth) + k

        sll $t2, $t2, 2                 # make t2 word readable
        add $t0, $t0, $t2               # t0 = &window[(l * windowWidth) + k]

        lw $t2, 0($t0)                  # t2 = window[(l * windowWidth) + k]

        sub $t0, $t4, $t2               # difference between t4 and t2
        slt $t2, $t0, $zero             # check if (t4 - t2) < 0
        bne $t2, $zero, sign_change             # jump to sign change

        sum:    add $s4, $s4, $t0       # add to absolute sum
                lw $t4, 0($sp)
                lw $t2, 4($sp)
                lw $t0, 8($sp)
                addi $sp, $sp, 12
                jr $ra

        sign_change:
        li $t2, -1 
        mul $t0, $t0, $t2               # change negative to positive
        j sum
          
   
